
public class TestConcatString {
	public static void main(String[] args) {
		String name1 = "Java";
		String name2 = "Programming";
		String msg = "Hello";
		int num = 123;
		System.out.println(name1 + name2);
		System.out.println(name1 + " " + name2);
		System.out.println(msg + num);
		System.out.println("My number is " + num);
	}
}
